-- MATA PELAJARAN
DELIMITER $$
CREATE TRIGGER `before_insert_mata_pelajaran`
BEFORE INSERT ON `mata_pelajaran`
FOR EACH ROW
BEGIN
    IF NEW.id IS NULL OR NEW.id = '' THEN
        SET NEW.id = CONCAT('MP', UUID());
    END IF;
END$$
DELIMITER ;

